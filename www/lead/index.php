				<h1><span>Selection Process</span></h1>

				<h2>Executive Administration</h2>
				<ol>
					<li>Current Executives meet to curate applications for President and Vice President</li>
					<li>Relevant Department Directors and Leads verify participation and disapprove or approve of applicants</li>
					<li>Candidates contacted for interview with all Executives</li>
					<li>Executives prepare cases for/against each applicant to present to all members of Administration</li>
					<li>Final selection by all members of Administration</li>
					<li>Give new Adminstration a trial run with supervision</li>
				</ol>

				<h2>Department Directors</h2>
				<p><em>Happens after new Executives are selected</em></p>
				<ol>
					<li>Current and new Executives curate applications</li>
					<li>Relevant Department Directors and Leads verify participation and disapprove or approve of applicants</li>
					<li>Candidates contacted for interview with </li>
					<ol>
						<li>Relevant director (both this year only)</li>
						<li>Old and new VP</li>
						<li>President</li>
					</ol>
					<li>Interviewers prepare cases for/against each applicant to present to all members of Administration</li>
					<li>Final selection by all members of administration</li>
