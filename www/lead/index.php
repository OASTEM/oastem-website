			<div class="post">
				<h1><span>Position Descriptions</span></h1>
				<p>All descriptions are subject to change - please keep updated on what responsibilities are under each position.</p>

				<h2>President</h2>
				<p><em>Treasury and Administration</em></p>
				<ul>
					<li>Provides direction for and maintains discipline of all of Oxford Academy STEM</li>
					<li>Maintains member roster and processes new applications</li>
					<li>Handles all-STEM communication, chairs general meetings, and banquet</li>
					<li>Manages Oxford Academy STEM Administration, including intra-STEM communication, regular Admin meetings, the Admin application process, and training new members of Administration</li>
					<li>Organizes with the Executive Advisor (faculty advisor)</li>
					<li>Tracks income and expenditures</li>
					<ul><li>Approves spending on competition material and registration fees</li></ul>
					<li>Assesses performance of Oxford Academy STEM as a whole and drafts a plan for future years</li>
					<li>Miscellaneous tasks</li>
				</ul>

				<h2>Vice President</h2>
				<p><em>Cooperation, Scheduling, and Inventory</em></p>
				<ul>
					<li>Promotes cooperation, resolves conflicts between Departments</li>
					<li>Organizes STEMinar Saturday by scheduling the mentor-led labs and lectures organized by Directors</li>
					<li>Approves curriculums proposed by the Department Directors and ensures that the Departments are following their own curriculum</li>
					<li>Administers Room 201, including partitioning out times, resolving any issues with room neighbors (i.e. the science teachers and the main office), and keeping it clean</li>
					<li>Maintains a calendar of Department meetings, STEMinar Saturdays, and competition dates (including but not limited to registration and competition dates) on paper in Room 201, online at oastem.org, and on the ASB calendar</li>
					<li>Inventories and organizes all items and materials</li>
					<li>Should any Project or Competition involve more than simply arranging rides between parents (eg hotels, buses, planes, etc), the Vice President will collaborate with the Lead on a trip itinerary</li>
				</ul>

				<h2>Department Directors</h2>
				<p>(including Sciences, Technology, Engineering, Math, Design, and Outreach) &mdash; <em>Education, Selection, and Extracurricular Opportunities</em></p>
				<ul>
					<li>Schedules a curriculum for the entire year and creates agendas for each meeting based on it</li>
					<li>Holds weekly Department Meetings to teach students the basics of their Department through lectures, group collaboration, or interactive demos relating to their Department</li>
					<ul><li>For example, the Technology Director would teach basic Java at Department meetings</li></ul>
					<li>Submits to the Vice President mentor-led labs and lectures for STEMinar Saturdays</li>
					<li>Instill discipline in members of their Department</li>
					<li>Requests the President for spending on curriculum materials</li>
					<li>Recruits mentors relevant to their Department</li>
					<li>Assigns members of their Department to competitions in which they would excel and collaboratively selects Competition Captains and Project Managers with other relevant Department Directors</li>
					<li>Supervises all Leads under their jurisdiction to ensure that all projects and competitions are on track</li>
					<li>Aggregates research opportunities and summer programs relevant to their Department</li>
				</ul>

				<h2>Competition Captains and Project Managers (Leads)</h2>
				<p><em>Plan of Attack, Logistics, and Records</em></p>
				<ul>
					<li>Responsible for the success or failure of their project or competition</li>
					<li>Schedules a plan of attack for their project or competition</li>
					<li>Responsible for project- or competition-specific training of members</li>
					<ul><li>For example, the Director of Technology trains the members in Java, while the Competition Captain teaches members how Java is used in FIRST Robotics</li></ul>
					<li>Completes registration for their own competition after requesting the President for funds</li>
					<li>Submit requests to the President for the acquisition of project- or competition-specific material</li>
					<li>Records relevant dates and submits them to the Vice President including but not limited to the dates when registration for their competition opens and closes and the actual date of their competition.</li>
					<li>Arranges competition logistics with the Volunteer Coordinator, including competition staff, food, transport, supervision, and so on</li>
					<ul><li>Should logistics extend past simply arranging rides between parents (e.g. hotels, buses, planes, etc), they will collaborate with the Vice President on an itinerary as well</li></ul>
					<li>Logs project or competition results and all other relevant info (changelog, performance, etc)</li>
				</ul>
